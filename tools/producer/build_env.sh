VIVID_FLATPAK_GBM_USAGE_SHIM="${VIVID_FLATPAK_GBM_USAGE_SHIM:-${VIVID_FLATPAK_GBM_SHIM_BUILD_DIR}/libvivid-gbm-usage-shim.so}"
VIVID_FLATPAK_SCENE_LIBRARY="${VIVID_FLATPAK_SCENE_LIBRARY:-${VIVID_FLATPAK_SCENE_BUILD_DIR}/out/${VIVID_SCENE_LIBRARY_NAME}}"
